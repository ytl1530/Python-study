"""
字符串常用操作
    str.lower()          将str字符串全部转为小写字母,结果为一个新的字符串
    str.upper()          将str字符串全部转为大写字母,结果为一个新的字符串
    str.split(sep=None)  把str按照指定的分隔符sep进行分隔,结果为列表类型
    str.count(sub)       结果为sub这个字符在str中出现的次数
    str.find(sub)        查询sub这个字符在str中是否存在,如果不存在结果为-1,如果存在结果为sub首次出现的索引
    str.index(sub)       功能与find()相同,区别在于要查询的字符sub不存在时,程序报错
    str.startswith(s)    查询字符串str是否以字符s开头
    str.endswith(s)      查询字符串str是否以字符s结尾
"""